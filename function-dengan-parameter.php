<?php
function hitungTotal($harga, $jumlah){
    return $harga * $jumlah;
}

echo hitungTotal(5000, 100);

function penjumlahan($nilai1, $nilai2){
    return $nilai1 + $nilai2;
}

function pengurangan($nilai1, $nilai2){
    return $nilai1 - $nilai2;
}

echo penjumlahan(10, 5);
echo pengurangan(20, 5);


?>